namespace GtMotive.Estimate.Microservice.Domain.Enums
{
    /// <summary>
    /// Represents the type of a vehicle.
    /// </summary>
    public enum VehicleType
    {
        /// <summary>
        /// A sedan is a type of car that typically has four doors, a closed roof, and a separate trunk.
        /// </summary>
        Sedan,

        /// <summary>
        /// An SUV (Sport Utility Vehicle) is a type of vehicle that combines elements of road-going passenger cars with features from off-road vehicles.
        /// </summary>
        SUV,

        /// <summary>
        /// A hatchback is a car with a hatch-type rear door that opens upwards and often a shared volume for the passenger and cargo areas.
        /// </summary>
        Hatchback,

        /// <summary>
        /// A convertible, also known as a cabriolet, is a type of vehicle that can convert between an open-air mode and a roofed one.
        /// </summary>
        Convertible,

        /// <summary>
        /// A van is a type of vehicle used for transporting goods or people. It is usually larger than a car and smaller than a truck.
        /// </summary>
        Van,

        /// <summary>
        /// A coupe is a car with a fixed-roof body style that is shorter than a sedan or saloon of the same model.
        /// </summary>
        Coupe,

        /// <summary>
        /// A truck is a large, heavy motor vehicle used for transporting goods, materials, or troops.
        /// </summary>
        Truck,

        /// <summary>
        /// A motorcycle is a two- or three-wheeled motor vehicle.
        /// </summary>
        Motorcycle
    }
}
